# Vamos a declarar una lista de numeros
numeros = [1,2,"Javascript",4,5]
print(numeros)
# Asignamos el segundo indice a una variable
tercero = numeros[2]
# Imprimimos en pantalla
print("El tercer elemento de la lista es:", tercero)

# Modificamos el cuarto de la lista
numeros[3] = "Python"
print(numeros)
# Imprimimos en pantalla
print("El cuarto elemento de la lista es:", numeros[3])