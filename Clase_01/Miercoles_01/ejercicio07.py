# Vamos a sumar y multiplicar elementos de dos tuplas de n√∫meros enteros:
tuplaUno = (1,2,3)
tuplaDos = (4,5,6)

# Vamos a realizar operaciones aritmeticas
## Suma
suma = (tuplaUno[0] + tuplaDos[0], tuplaUno[1] + tuplaDos[1], tuplaUno[2] + tuplaDos[2])
## Producto entre tuplas
producto = (tuplaUno[0] * tuplaDos[0], tuplaUno[1] * tuplaDos[1], tuplaUno[2] * tuplaDos[2])

# Imprimimos las operaciones en pantalla
print(suma)
print(producto)